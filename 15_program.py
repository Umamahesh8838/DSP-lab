# List operations: add, remove, and find length
my_list = [1, 2, 3, 4]

# Add an element
my_list.append(5)
print("List after adding 5:", my_list)

# Remove an element
my_list.remove(2)
print("List after removing 2:", my_list)

# Find the length of the list
length = len(my_list)
print("Length of the list:", length)
