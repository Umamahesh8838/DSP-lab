def literals_demo():
    # Integer literals
    decimal = 100  # Decimal literal
    octal = 0o144  # Octal literal (prefix 0o)
    hex = 0x64  # Hexadecimal literal (prefix 0x)
    binary = 0b1100100  # Binary literal (prefix 0b)

    # Floating-point literals
    float_literal = 10.5  # Float literal
    double_literal = 10.5  # Double literal (Python uses double precision for floats)

    # Character literals (Python does not have a separate character type, uses strings)
    char_literal = 'A'  # Character literal
    unicode_literal = '\u0041'  # Unicode literal

    # String literal
    string_literal = "Hello, World!"

    # Boolean literals
    true_literal = True
    false_literal = False

    # Printing the literals
    print("Integer Literals:")
    print("Decimal:", decimal)
    print("Octal:", octal)
    print("Hexadecimal:", hex)
    print("Binary:", binary)

    print("\nFloating-point Literals:")
    print("Float:", float_literal)
    print("Double:", double_literal)

    print("\nCharacter Literals:")
    print("Character:", char_literal)
    print("Unicode:", unicode_literal)

    print("\nString Literal:")
    print("String:", string_literal)

    print("\nBoolean Literals:")
    print("True:", true_literal)
    print("False:", false_literal)

literals_demo()
