# Dictionary operations
my_dict = {"name": "Uma", "age": 20}

# Add an element
my_dict["city"] = "Faridabad"
print("Dictionary after adding city:", my_dict)

# Remove an element
my_dict.pop("age")
print("Dictionary after removing age:", my_dict)

# Update an element
my_dict["name"] = "Mahesh"
print("Dictionary after updating name:", my_dict)
